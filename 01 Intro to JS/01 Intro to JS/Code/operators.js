//arithmetic operators
console.log(2 + 5);
console.log(5 / 2); //2.5
console.log(Math.floor(5 / 2)); //2
console.log(5 % 2); //remainder ie 1

//assigment operators
let x = 2;
x += 2;
console.log(x); //4
x /= 2;
console.log(x); //2
x %= 2;
console.log(x); //0

//logical operators
console.log(3 < 5 && 9 < 3);
console.log(4 || 0); //4
console.log(4 && 0); //0
console.log(0 || 4); //4
console.log(0 && 4); //0
